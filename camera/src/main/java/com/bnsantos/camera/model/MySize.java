package com.bnsantos.camera.model;

public class MySize {
  private final int mWidth;
  private final int mHeight;

  public MySize(int width, int height) {
    this.mWidth = width;
    this.mHeight = height;
  }

  public int getWidth() {
    return mWidth;
  }

  public int getHeight() {
    return mHeight;
  }
}
